// Combine two strings
const string1 = "Hello, ";
const string2 = "world!";
const combinedString = string1 + string2;
console.log(combinedString); // Output: Hello, world!

// Add your name to a greeting message
const name = "Sampath Kumar";
const greetingMessage = `${combinedString} My name is ${name}.`;
console.log(greetingMessage); // Output: Hello, world! My name is Sampath Kumar.

// Use replace() to change a word in a sentence
const originalSentence = "The weather is nice today.";
const modifiedSentence = originalSentence.replace("nice", "beautiful");
console.log(modifiedSentence); // Output: The weather is beautiful today.
